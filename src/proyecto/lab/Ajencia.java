package proyecto.lab;

public class Ajencia {

    String nombre, cAvion, uAsiento, cAsiento, destino;
    double cBoleto;
    int nAsiento, identificaciones;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getcAvion() {
        return cAvion;
    }

    public void setcAvion(String cAvion) {
        this.cAvion = cAvion;
    }

    public String getuAsiento() {
        return uAsiento;
    }

    public void setuAsiento(String uAsiento) {
        this.uAsiento = uAsiento;
    }

    public String getcAsiento() {
        return cAsiento;
    }

    public void setcAsiento(String cAsiento) {
        this.cAsiento = cAsiento;
    }

    public String getDestino() {
        return destino;
    }

    public void setDestino(String destino) {
        this.destino = destino;
    }

    public double getcBoleto() {
        return cBoleto;
    }

    public void setcBoleto(double cBoleto) {
        this.cBoleto = cBoleto;
    }

    public int getnAsiento() {
        return nAsiento;
    }

    public void setnAsiento(int nAsiento) {
        this.nAsiento = nAsiento;
    }

    public int getIdentificaciones() {
        return identificaciones;
    }

    public void setIdentificaciones(int identificaciones) {
        this.identificaciones = identificaciones;
    }

}
